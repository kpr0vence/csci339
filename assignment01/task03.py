# 3. Input a one page long file and process the text into words. Also get rid of "stop words".

def process(data):
    words = split_and_clean(data)
    words = clear_stop_words(words)
    return words

def split_and_clean(sentence):
    tokens = sentence.split(" ")
    for i in range(len(tokens)):
        if not tokens[i].isalnum():
            tokens[i] = re.sub(r"[^a-zA-Z0-9\s]", "", tokens[i])            

    return tokens

